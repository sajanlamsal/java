public class Player {


    private String name;
    private int score;
    private int highScore;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getHighScore() {
        return  highScore;
    }

    public void setHighScore(int score) {
        this.highScore = highScore;
    }


}
